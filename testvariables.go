package main

import "fmt"

func main() {
	/* Pembubuhan value 1 baris */
	var nama string = "David Maulana"
	fmt.Println(nama)

	/* Pembubuhan value baris berikutnya */
	var umur int
	umur = 20
	fmt.Println(umur)

	alamat := "Jakarta"
	fmt.Println(alamat)

	alamat = "Surabaya"
	fmt.Println(alamat)

	//alamat = 12
	fmt.Println(alamat)

}
